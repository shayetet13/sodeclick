const User = require('../models/User');

// Middleware ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏≠‡∏≠‡∏ü‡πÑ‡∏•‡∏ô‡πå‡∏Ç‡∏≠‡∏á‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡∏£‡∏∞‡∏ö‡∏ö‡πÄ‡∏õ‡πá‡∏ô‡πÄ‡∏ß‡∏•‡∏≤‡∏ô‡∏≤‡∏ô
const updateOfflineStatus = async () => {
  try {
    console.log('üîÑ Checking for users to set offline...');
    
    // ‡∏´‡∏≤‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏ó‡∏µ‡πà‡∏≠‡∏≠‡∏ô‡πÑ‡∏•‡∏ô‡πå‡πÅ‡∏ï‡πà‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡∏£‡∏∞‡∏ö‡∏ö‡∏°‡∏≤‡∏Å‡∏Å‡∏ß‡πà‡∏≤ 30 ‡∏ô‡∏≤‡∏ó‡∏µ
    const thirtyMinutesAgo = new Date(Date.now() - 30 * 60 * 1000);
    
    const usersToSetOffline = await User.find({
      isOnline: true,
      lastActive: { $lt: thirtyMinutesAgo }
    });
    
    if (usersToSetOffline.length > 0) {
      console.log(`üì¥ Setting ${usersToSetOffline.length} users offline`);
      
      // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏≠‡∏≠‡∏ü‡πÑ‡∏•‡∏ô‡πå
      await User.updateMany(
        {
          isOnline: true,
          lastActive: { $lt: thirtyMinutesAgo }
        },
        {
          $set: { isOnline: false }
        }
      );
      
      console.log(`‚úÖ Successfully set ${usersToSetOffline.length} users offline`);
    } else {
      console.log('‚úÖ No users need to be set offline');
    }
    
  } catch (error) {
    console.error('‚ùå Error updating offline status:', error);
  }
};

