import React, { createContext, useContext, useState, useEffect } from 'react';

const AuthContext = createContext();

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [loading, setLoading] = useState(true);
  const [idleTimer, setIdleTimer] = useState(null);
  const [warningTimer, setWarningTimer] = useState(null);
  const [showIdleWarning, setShowIdleWarning] = useState(false);

  // Auto sign out after 15 minutes of inactivity
  const IDLE_TIMEOUT = 15 * 60 * 1000; // 15 minutes in milliseconds
  const WARNING_TIME = 14 * 60 * 1000; // Show warning at 14 minutes

  // Reset idle timer
  const resetIdleTimer = () => {
    // Clear all existing timers first
    if (idleTimer) {
      clearTimeout(idleTimer);
      setIdleTimer(null);
    }
    if (warningTimer) {
      clearTimeout(warningTimer);
      setWarningTimer(null);
    }
    
    // Hide warning modal if it's showing
    setShowIdleWarning(false);

    if (user) {
      console.log('üîÑ Resetting idle timer - fresh 15 minutes');
      
      // Set warning timer (14 minutes)
      const warningTimerId = setTimeout(() => {
        setShowIdleWarning(true);
        console.log('‚ö†Ô∏è Idle warning: 1 minute left before auto sign out');
      }, WARNING_TIME);

      // Set auto sign out timer (15 minutes)
      const timerId = setTimeout(() => {
        console.log('üö™ Auto sign out due to inactivity');
        logout();
      }, IDLE_TIMEOUT);

      setWarningTimer(warningTimerId);
      setIdleTimer(timerId);
    }
  };

  // Handle user activity with debouncing to prevent excessive timer resets
  let activityTimeout = null;
  const handleUserActivity = () => {
    if (user) {
      // Debounce activity detection - reset timer only once per second
      if (activityTimeout) {
        clearTimeout(activityTimeout);
      }
      
      activityTimeout = setTimeout(() => {
        console.log('üîÑ User activity detected, resetting idle timer');
        resetIdleTimer();
      }, 1000); // Reset timer max once per second
    }
  };

  useEffect(() => {
    // Check if user is logged in on app start
    const token = sessionStorage.getItem('token');
    const userData = sessionStorage.getItem('user');
    
    if (token && userData) {
      try {
        const parsedUser = JSON.parse(userData);
        
        // Check for invalid user IDs (old deleted users)
        const invalidUserIds = [
          '68c13cb085d17f0b0d4584bc', // Old kao user ID
          '68bd5debcf52bbadcf865456', // test user
          '68bd5f2ecf52bbadcf86595d', // user_829394452
          '68bd7531cf52bbadcf865b67', // K.nampetch
          '68bdaa833750baa9df62c22d'  // Achi
          // Removed '68bdab749a77b0ed80649af6' - admin user should be valid
        ];
        
        if (invalidUserIds.includes(parsedUser._id)) {
          console.log('üö® Invalid user ID detected, clearing session:', parsedUser._id);
          sessionStorage.removeItem('token');
          sessionStorage.removeItem('user');
          setUser(null);
        } else {
          setUser(parsedUser);
        }
      } catch (error) {
        console.error('Error parsing user data:', error);
        sessionStorage.removeItem('token');
        sessionStorage.removeItem('user');
      }
    }
    setLoading(false);
    
    // Handle browser close/refresh - update online status to false
    const handleBeforeUnload = (event) => {
      const token = sessionStorage.getItem('token');
      if (token) {
        // ‡πÉ‡∏ä‡πâ fetch with keepalive ‡πÅ‡∏ó‡∏ô sendBeacon ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏™‡πà‡∏á headers ‡πÑ‡∏î‡πâ‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á
        fetch(`${import.meta.env.VITE_API_BASE_URL || 'http://localhost:5000'}/api/auth/logout`, {
          method: 'POST',
          headers: {
            'Authorization': `Bearer ${token}`,
            'Content-Type': 'application/json'
          },
          body: JSON.stringify({}),
          keepalive: true // ‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç! ‡∏ó‡∏≥‡πÉ‡∏´‡πâ request ‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡∏ï‡πà‡∏≠‡πÅ‡∏°‡πâ page ‡∏õ‡∏¥‡∏î
        }).catch(err => {
          console.error('‚ùå Failed to logout on beforeunload:', err);
        });
        console.log('üî¥ Browser closing: Sending logout request');
      }
    };
    
    // ‡πÄ‡∏û‡∏¥‡πà‡∏° handler ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö visibility change ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ï‡∏£‡∏ß‡∏à‡∏à‡∏±‡∏ö‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏õ‡∏¥‡∏î tab
    const handleVisibilityChange = () => {
      if (document.hidden) {
        const token = sessionStorage.getItem('token');
        if (token) {
          // ‡πÄ‡∏°‡∏∑‡πà‡∏≠ tab ‡∏ñ‡∏π‡∏Å‡∏ã‡πà‡∏≠‡∏ô (‡∏≠‡∏≤‡∏à‡∏à‡∏∞‡∏õ‡∏¥‡∏î) ‡πÉ‡∏´‡πâ‡∏≠‡∏±‡∏û‡πÄ‡∏î‡∏ó‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞ lastActive
          console.log('üì¥ Tab hidden, updating lastActive');
        }
      }
    };
    
    window.addEventListener('beforeunload', handleBeforeUnload);
    document.addEventListener('visibilitychange', handleVisibilityChange);
    
    return () => {
      window.removeEventListener('beforeunload', handleBeforeUnload);
      document.removeEventListener('visibilitychange', handleVisibilityChange);
    };
  }, []);

  // Set up idle timer when user logs in
  useEffect(() => {
    if (user) {
      resetIdleTimer();
    } else {
      // Clear timers when user logs out
      if (idleTimer) {
        clearTimeout(idleTimer);
        setIdleTimer(null);
      }
      if (warningTimer) {
        clearTimeout(warningTimer);
        setWarningTimer(null);
      }
      setShowIdleWarning(false);
    }
  }, [user]);

  // Set up activity listeners
  useEffect(() => {
    if (user) {
      // ‡πÄ‡∏û‡∏¥‡πà‡∏° event listeners ‡∏°‡∏≤‡∏Å‡∏Ç‡∏∂‡πâ‡∏ô‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ï‡∏£‡∏ß‡∏à‡∏à‡∏±‡∏ö activity ‡πÑ‡∏î‡πâ‡∏î‡∏µ‡∏Ç‡∏∂‡πâ‡∏ô
      const events = [
        'mousedown', 'mousemove', 'mouseup', 'mouseover', 'mouseout',
        'keypress', 'keydown', 'keyup',
        'scroll', 'wheel',
        'touchstart', 'touchend', 'touchmove',
        'click', 'dblclick',
        'focus', 'blur',
        'resize',
        'visibilitychange' // ‡∏ï‡∏£‡∏ß‡∏à‡∏à‡∏±‡∏ö‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏Å‡∏•‡∏±‡∏ö‡∏°‡∏≤‡πÉ‡∏ä‡πâ tab
      ];
      
      // ‡πÉ‡∏ä‡πâ passive listeners ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏õ‡∏£‡∏∞‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡∏†‡∏≤‡∏û‡∏ó‡∏µ‡πà‡∏î‡∏µ‡∏Ç‡∏∂‡πâ‡∏ô
      events.forEach(event => {
        document.addEventListener(event, handleUserActivity, { passive: true, capture: true });
      });

      // ‡∏ï‡∏£‡∏ß‡∏à‡∏à‡∏±‡∏ö‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏Å‡∏•‡∏±‡∏ö‡∏°‡∏≤‡πÉ‡∏ä‡πâ tab/window
      const handleVisibilityChange = () => {
        if (!document.hidden && user) {
          console.log('üëÄ User returned to tab, resetting idle timer');
          handleUserActivity();
        }
      };

      document.addEventListener('visibilitychange', handleVisibilityChange);

      return () => {
        events.forEach(event => {
          document.removeEventListener(event, handleUserActivity, { passive: true, capture: true });
        });
        document.removeEventListener('visibilitychange', handleVisibilityChange);
      };
    }
  }, [user]);

  const login = (userData) => {
    console.log('üîç AuthContext login called with:', userData);
    const userToSet = userData.user || userData;
    console.log('üîç User to set:', userToSet);
    console.log('üîç User ID in userToSet:', userToSet._id || userToSet.id || userToSet.userId);
    
    setUser(userToSet);
    sessionStorage.setItem('token', userData.token || userData.data?.token);
    sessionStorage.setItem('user', JSON.stringify(userToSet));
    
    // Send login event
    window.dispatchEvent(new CustomEvent('userLoggedIn', { 
      detail: { user: userData.user || userData } 
    }));
    
    // Reset idle timer after login
    resetIdleTimer();
    
    console.log('‚úÖ Login successful, user state updated');
  };

  const logout = () => {
    console.log('üö™ Logging out...');
    const token = sessionStorage.getItem('token');
    
    // ‡∏≠‡∏±‡∏û‡πÄ‡∏î‡∏ó online status ‡πÄ‡∏õ‡πá‡∏ô false
    if (token) {
      fetch(`${import.meta.env.VITE_API_BASE_URL || 'http://localhost:5000'}/api/auth/logout`, {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${token}`,
          'Content-Type': 'application/json'
        }
      }).then(() => {
        console.log('‚úÖ Logout: Online status updated to false');
      }).catch((err) => {
        console.error('‚ùå Logout: Failed to update online status:', err);
      });
    }
    
    // ‡∏•‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÅ‡∏•‡∏∞‡∏™‡πà‡∏á event
    setUser(null);
    sessionStorage.removeItem('token');
    sessionStorage.removeItem('user');
    window.dispatchEvent(new CustomEvent('userLoggedOut'));
    
    // ‡∏£‡∏µ‡πÄ‡∏ü‡∏£‡∏ä‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏ß‡πá‡∏ö‡∏≠‡∏±‡∏ï‡πÇ‡∏ô‡∏°‡∏±‡∏ï‡∏¥‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏•‡πá‡∏≠‡∏Å‡πÄ‡∏≠‡∏≤‡∏ï‡πå
    console.log('üîÑ Refreshing page after logout');
    setTimeout(() => {
      window.location.reload();
    }, 500);
  };

  // Function to validate current user and force logout if invalid
  const validateUser = async () => {
    const token = sessionStorage.getItem('token');
    if (!token || !user) return true;

    try {
      const response = await fetch(`${import.meta.env.VITE_API_BASE_URL || 'http://localhost:5000'}/api/auth/me`, {
        headers: {
          'Authorization': `Bearer ${token}`,
          'Content-Type': 'application/json'
        }
      });

      if (!response.ok) {
        console.log('üö® User validation failed, logging out');
        logout();
        return false;
      }

      const data = await response.json();
      if (!data.success) {
        console.log('üö® User validation failed, logging out');
        logout();
        return false;
      }

      return true;
    } catch (error) {
      console.error('‚ùå User validation error:', error);
      logout();
      return false;
    }
  };

  // Function to dismiss idle warning
  const dismissIdleWarning = async () => {
    console.log('‚úÖ User dismissed idle warning, resetting timer');
    
    // Clear all existing timers first
    if (idleTimer) {
      clearTimeout(idleTimer);
      setIdleTimer(null);
    }
    if (warningTimer) {
      clearTimeout(warningTimer);
      setWarningTimer(null);
    }
    
    // Hide the warning modal
    setShowIdleWarning(false);
    
    // Reset the idle timer to start fresh
    if (user) {
      console.log('üîÑ Restarting idle timer after user confirmation');
      
      // Set warning timer (14 minutes)
      const warningTimerId = setTimeout(() => {
        setShowIdleWarning(true);
        console.log('‚ö†Ô∏è Idle warning: 1 minute left before auto sign out');
      }, WARNING_TIME);

      // Set auto sign out timer (15 minutes)
      const timerId = setTimeout(() => {
        console.log('üö™ Auto sign out due to inactivity');
        logout();
      }, IDLE_TIMEOUT);

      setWarningTimer(warningTimerId);
      setIdleTimer(timerId);
    }
  };

  const value = {
    user,
    login,
    logout,
    validateUser,
    loading,
    showIdleWarning,
    dismissIdleWarning
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
};
